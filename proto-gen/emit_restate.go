// Restate-mode emission for protoc-gen-es-go. Activated when buf
// invokes the plugin with opt: runtime=restate. The plugin emits
// only Restate service handlers in this mode; the core sum_type /
// codec / projection codegen is skipped.
//
// See ADR 0026 § 3.
package main

import (
	"fmt"

	"google.golang.org/protobuf/compiler/protogen"
	"google.golang.org/protobuf/proto"

	esv1 "github.com/laenenai/eventstore/gen/es/v1"
)

// Import paths the generated Restate-handler code refers to.
var (
	cmdwfPkg     = protogen.GoImportPath("github.com/laenenai/eventstore/cmdworkflow")
	cwrestatePkg = protogen.GoImportPath("github.com/laenenai/eventstore/adapters/cmdworkflow/restate")
	restateSDK   = protogen.GoImportPath("github.com/restatedev/sdk-go")
)

// aggregate captures one State message + its Command sum-type variants.
type aggregate struct {
	name      string             // aggregate short name (e.g., "invoice")
	state     *protogen.Message  // the State message
	commands  []*protogen.Message
	commandSumTypeName string    // the Go interface name (usually "Command")
	eventSumTypeName   string    // the Go interface name (usually "Event")
	eventCodecName     string    // the codec struct name (commandSumTypeName + "Codec")

	// Populated by generateRestateFile before emitRestateService.
	// The Restate-mode file lives in a different Go package; we need
	// the root package path to qualify type references properly.
	rootImport  protogen.GoImportPath
	rootPkgName string
}

// commandVariantInfo records the annotated fields on one command.
type commandVariantInfo struct {
	msg          *protogen.Message
	streamIDFld  *protogen.Field
	tenantIDFld  *protogen.Field
}

// generateRestateFile is called by main.go when runtime=restate is
// configured. Emits handler files; the core sum_type/codec/projection
// emission is skipped in this mode.
func generateRestateFile(plugin *protogen.Plugin, file *protogen.File) error {
	aggregates, err := findAggregatesWithAnnotatedCommands(file, plugin.Files)
	if err != nil {
		return err
	}
	if len(aggregates) == 0 {
		return nil
	}

	// The Restate-mode output lives in a DIFFERENT module
	// (adapters/cmdworkflow/restate/gen/...) than the core gen tree.
	// Compute a distinct GoImportPath so protogen properly qualifies
	// references to the core types (Invoice, Command, etc.) with an
	// import alias instead of treating them as same-package.
	//
	// The package name gains a "restate" suffix to avoid the
	// invoicev1-vs-invoicev1 ambiguity at the call site.
	rootImport := string(file.GoImportPath)
	restateImport := protogen.GoImportPath(
		"github.com/laenenai/eventstore/adapters/cmdworkflow/restate/gen" +
			rootImport[len("github.com/laenenai/eventstore/gen"):],
	)
	restatePkgName := string(file.GoPackageName) + "restate"

	out := plugin.NewGeneratedFile(
		file.GeneratedFilenamePrefix+"_restate_es.pb.go",
		restateImport,
	)
	out.P("// Code generated by protoc-gen-es-restate. DO NOT EDIT.")
	out.P("// source: ", file.Desc.Path())
	out.P()
	out.P("package ", restatePkgName)
	out.P()

	// Remember the root import path so emitRestateService can
	// reference Invoice/Command via the root package.
	for _, agg := range aggregates {
		agg.rootImport = file.GoImportPath
		agg.rootPkgName = string(file.GoPackageName)
	}

	for _, agg := range aggregates {
		variants, err := collectVariants(agg)
		if err != nil {
			return err
		}
		emitRestateService(out, agg, variants)
	}
	return nil
}

// findAggregatesWithAnnotatedCommands finds State messages
// (option (es.v1.aggregate) = "X") and pairs them with Command sum
// types in the same file whose variants are annotated.
//
// The Event sum type is resolved across every file in the same proto
// package (not just the file declaring the Command sum type). Real-
// world layouts split aggregates across multiple proto files —
// state + commands in one, events in another — and the package is
// the right boundary: same proto package = same aggregate scope, and
// the framework already isolates aggregates across packages by
// convention. Files from different packages are never consulted for
// the Event sum-type pairing.
//
// Skips aggregates whose commands have NO annotations (opt-in).
// Errors if some — but not all — variants have annotations, or if a
// variant has stream_id but not tenant_id (or vice versa).
func findAggregatesWithAnnotatedCommands(file *protogen.File, allFiles []*protogen.File) ([]*aggregate, error) {
	// 1. Collect State messages.
	stateByName := map[string]*aggregate{}
	for _, m := range file.Messages {
		aggName, _ := proto.GetExtension(m.Desc.Options(), esv1.E_Aggregate).(string)
		if aggName == "" {
			continue
		}
		stateByName[aggName] = &aggregate{name: aggName, state: m}
	}
	if len(stateByName) == 0 {
		return nil, nil
	}

	// 2. Find Command sum-type and check variant annotations.
	for _, m := range file.Messages {
		sumName, _ := proto.GetExtension(m.Desc.Options(), esv1.E_SumType).(string)
		if sumName == "" {
			continue
		}
		if len(m.Oneofs) != 1 {
			return nil, fmt.Errorf("%s: sum_type message must have exactly one oneof", m.GoIdent.GoName)
		}
		// Only Command sum-types map to workflow handlers. Skip Event
		// sum-types and arbitrary other sum-types.
		if sumName != "Command" {
			continue
		}

		// Check each variant for annotations. Either ALL have both
		// stream_id + tenant_id, or NONE do (skip whole aggregate).
		anyAnnotated, allAnnotated := false, true
		for _, f := range m.Oneofs[0].Fields {
			variant := f.Message
			if variant == nil {
				return nil, fmt.Errorf("%s.%s: oneof field is not a message variant", m.GoIdent.GoName, f.GoName)
			}
			has := hasStreamID(variant) && hasTenantID(variant)
			if hasStreamID(variant) || hasTenantID(variant) {
				anyAnnotated = true
			}
			if !has {
				allAnnotated = false
			}
		}
		if !anyAnnotated {
			continue // unannotated → no Restate service for this aggregate
		}
		if !allAnnotated {
			// Some annotated, some not — partial. Identify offenders.
			for _, f := range m.Oneofs[0].Fields {
				v := f.Message
				if !hasStreamID(v) {
					return nil, fmt.Errorf("%s: command variant %s is missing a field annotated [(es.v1.stream_id) = true]",
						file.Desc.Path(), v.GoIdent.GoName)
				}
				if !hasTenantID(v) {
					return nil, fmt.Errorf("%s: command variant %s is missing a field annotated [(es.v1.tenant_id) = true]",
						file.Desc.Path(), v.GoIdent.GoName)
				}
			}
		}

		// Locate the aggregate this Command set belongs to. The file
		// must have exactly one aggregate when it has annotated
		// commands — multi-aggregate files would need explicit
		// linking.
		if len(stateByName) > 1 {
			return nil, fmt.Errorf("%s: annotated commands but multiple aggregates declared in one file (%d)",
				file.Desc.Path(), len(stateByName))
		}
		var agg *aggregate
		for _, a := range stateByName {
			agg = a
			break
		}
		agg.commandSumTypeName = sumName
		for _, f := range m.Oneofs[0].Fields {
			agg.commands = append(agg.commands, f.Message)
		}
	}

	// 3. Resolve the Event sum type for each aggregate that has
	// commands. The codegen-emitted Workflow constructor takes the
	// EventCodec as a fourth argument (ADR 0029), so we need the
	// Event sum type's interface name + the conventional codec name
	// (sumType + "Codec").
	//
	// Scan every file in the same proto package — real-world layouts
	// often split aggregates into a state+commands file and an events
	// file (centcom does this for individual, identity, kyc, etc.).
	// Cross-package scans are deliberately excluded: an Event sum-type
	// in a different package would couple aggregates across the
	// boundary the framework treats as their natural isolation seam.
	pkg := file.Desc.Package()
	for _, f := range allFiles {
		if f.Desc.Package() != pkg {
			continue
		}
		for _, m := range f.Messages {
			sumName, _ := proto.GetExtension(m.Desc.Options(), esv1.E_SumType).(string)
			if sumName != "Event" {
				continue
			}
			for _, a := range stateByName {
				if len(a.commands) > 0 {
					a.eventSumTypeName = sumName
					a.eventCodecName = sumName + "Codec"
				}
			}
		}
	}

	// 4. Return aggregates that actually have commands attached, with
	// a fatal error if Event sum type was missing — the runtime
	// Workflow needs an EventCodec.
	var out []*aggregate
	for _, a := range stateByName {
		if len(a.commands) > 0 {
			if a.eventSumTypeName == "" {
				return nil, fmt.Errorf("%s: aggregate %q has annotated commands but no Event sum type (option (es.v1.sum_type) = \"Event\") in the same proto package",
					file.Desc.Path(), a.name)
			}
			out = append(out, a)
		}
	}
	return out, nil
}

// collectVariants resolves each command variant to its annotated
// stream_id + tenant_id fields. Called after validation, so missing
// annotations are a bug, not user error.
func collectVariants(agg *aggregate) ([]commandVariantInfo, error) {
	out := make([]commandVariantInfo, 0, len(agg.commands))
	for _, cmd := range agg.commands {
		info := commandVariantInfo{msg: cmd}
		for _, f := range cmd.Fields {
			if isStreamIDField(f) {
				info.streamIDFld = f
			}
			if isTenantIDField(f) {
				info.tenantIDFld = f
			}
		}
		if info.streamIDFld == nil || info.tenantIDFld == nil {
			return nil, fmt.Errorf("%s: missing annotations (post-validation bug)", cmd.GoIdent.GoName)
		}
		out = append(out, info)
	}
	return out, nil
}

func hasStreamID(m *protogen.Message) bool {
	for _, f := range m.Fields {
		if isStreamIDField(f) {
			return true
		}
	}
	return false
}

func hasTenantID(m *protogen.Message) bool {
	for _, f := range m.Fields {
		if isTenantIDField(f) {
			return true
		}
	}
	return false
}

func isStreamIDField(f *protogen.Field) bool {
	v, _ := proto.GetExtension(f.Desc.Options(), esv1.E_StreamId).(bool)
	return v
}

func isTenantIDField(f *protogen.Field) bool {
	v, _ := proto.GetExtension(f.Desc.Options(), esv1.E_TenantId).(bool)
	return v
}

// emitRestateService writes the RestateService struct + one handler
// per command variant. Each handler is a thin wrapper around
// HandleCmd: build the StreamID from the annotated fields, propagate
// tenant via context, invoke.
func emitRestateService(out *protogen.GeneratedFile, agg *aggregate, variants []commandVariantInfo) {
	// Types defined in the root core gen package — Invoice, Command,
	// Event, Create, MarkPaid, Void — must be qualified through the
	// import. The Workflow is generic on [S, C, E] (ADR 0029), so
	// the Event sum type appears in the service struct's type
	// parameters too.
	stateName := out.QualifiedGoIdent(agg.rootImport.Ident(agg.state.GoIdent.GoName))
	cmdIface := out.QualifiedGoIdent(agg.rootImport.Ident(agg.commandSumTypeName))
	evtIface := out.QualifiedGoIdent(agg.rootImport.Ident(agg.eventSumTypeName))

	// Framework + adapter references.
	ctxType := out.QualifiedGoIdent(contextPkg.Ident("Context"))
	esWithTenant := out.QualifiedGoIdent(esPkg.Ident("WithTenant"))
	esNewStreamID := out.QualifiedGoIdent(esPkg.Ident("NewStreamID"))
	cmdwfWorkflow := out.QualifiedGoIdent(cmdwfPkg.Ident("Workflow"))
	withContext := out.QualifiedGoIdent(cwrestatePkg.Ident("WithContext"))
	restateCtx := out.QualifiedGoIdent(restateSDK.Ident("Context"))

	svcName := "RestateService"
	out.P("// ", svcName, " exposes the ", stateName, " aggregate as a Restate service.")
	out.P("// One handler per ", cmdIface, " variant; each wraps cmdworkflow.Workflow.HandleCmd")
	out.P("// with the tenant and stream id extracted from the command.")
	out.P("type ", svcName, " struct {")
	out.P("\tWorkflow *", cmdwfWorkflow, "[*", stateName, ", ", cmdIface, ", ", evtIface, "]")
	out.P("}")
	out.P()
	out.P("// New", svcName, " returns a Restate-bindable service backed by wf.")
	out.P("// Also wires durable Async fan-out: subsequent Async subscriber")
	out.P("// dispatches go through restate.ServiceSend targeting AsyncDispatch,")
	out.P("// keyed by <streamType>:<subscriberName>:<firstEventID> for dedup.")
	out.P("func New", svcName, "(wf *", cmdwfWorkflow, "[*", stateName, ", ", cmdIface, ", ", evtIface, "]) *", svcName, " {")
	out.P("\ts := &", svcName, "{Workflow: wf}")
	out.P("\twf.SetAsyncSend(s.sendAsync)")
	out.P("\treturn s")
	out.P("}")
	out.P()
	out.P("// ServiceName overrides the SDK's default reflection name so")
	out.P("// the Restate service URL is /", capitalize(agg.name), "/<handler> regardless of Go type names.")
	out.P("func (*", svcName, ") ServiceName() string { return \"", capitalize(agg.name), "\" }")
	out.P()

	// One method per command variant.
	for _, v := range variants {
		cmdName := out.QualifiedGoIdent(agg.rootImport.Ident(v.msg.GoIdent.GoName))
		streamGetter := "Get" + v.streamIDFld.GoName + "()"
		tenantGetter := "Get" + v.tenantIDFld.GoName + "()"
		bgCtx := out.QualifiedGoIdent(contextPkg.Ident("Background"))
		out.P("func (s *", svcName, ") ", v.msg.GoIdent.GoName, "(ctx ", restateCtx, ", cmd *", cmdName, ") (*", stateName, ", error) {")
		out.P("\ttenant := cmd.", tenantGetter)
		out.P("\tsid, err := ", esNewStreamID, "(tenant, \"", agg.name, "\", cmd.", streamGetter, ")")
		out.P("\tif err != nil { return nil, err }")
		out.P("\tstdCtx := ", withContext, "(", esWithTenant, "(", bgCtx, "(), tenant), ctx)")
		out.P("\treturn s.Workflow.HandleCmd(stdCtx, sid, cmd)")
		out.P("}")
		out.P()
	}

	// AsyncDispatch — registered with Restate as a separate handler.
	// Each Async subscriber × event becomes one child invocation
	// targeting this handler; the workflow id (set as Restate's
	// idempotency-key) ensures dedup across replays.
	cmdwfAsyncPayload := out.QualifiedGoIdent(cmdwfPkg.Ident("AsyncPayload"))
	cwRestateFromCtx := out.QualifiedGoIdent(cwrestatePkg.Ident("FromContext"))
	cwRestateErrNoCtx := out.QualifiedGoIdent(cwrestatePkg.Ident("ErrNoRestateContext"))
	restateServiceSend := out.QualifiedGoIdent(restateSDK.Ident("ServiceSend"))
	restateWithIdemKey := out.QualifiedGoIdent(restateSDK.Ident("WithIdempotencyKey"))
	bgCtx := out.QualifiedGoIdent(contextPkg.Ident("Background"))

	out.P("// AsyncDispatch is the Restate handler that runs an Async")
	out.P("// subscriber's delivery + retry + exhausted policy. Register")
	out.P("// it alongside the per-command handlers; the framework's")
	out.P("// spawnAsync dispatches to it on every Async subscriber × event.")
	out.P("func (s *", svcName, ") AsyncDispatch(ctx ", restateCtx, ", payload ", cmdwfAsyncPayload, ") (struct{}, error) {")
	out.P("\tstdCtx := ", withContext, "(", bgCtx, "(), ctx)")
	out.P("\treturn struct{}{}, s.Workflow.DispatchAsync(stdCtx, payload.SubscriberName, payload.EnvBytes)")
	out.P("}")
	out.P()
	out.P("// sendAsync is the AsyncSend wired into the Workflow at construction.")
	out.P("// Called from spawnAsync when an Async subscriber matches; issues a")
	out.P("// fire-and-forget ServiceSend to AsyncDispatch with the workflow id")
	out.P("// as Restate's idempotency-key.")
	out.P("func (s *", svcName, ") sendAsync(ctx ", ctxType, ", subscriberName string, envBytes []byte, workflowID string) error {")
	out.P("\trc, ok := ", cwRestateFromCtx, "(ctx)")
	out.P("\tif !ok { return ", cwRestateErrNoCtx, " }")
	out.P("\t", restateServiceSend, "(rc, \"", capitalize(agg.name), "\", \"AsyncDispatch\").Send(", cmdwfAsyncPayload, "{")
	out.P("\t\tSubscriberName: subscriberName,")
	out.P("\t\tEnvBytes:       envBytes,")
	out.P("\t}, ", restateWithIdemKey, "(workflowID))")
	out.P("\treturn nil")
	out.P("}")
	out.P()

	// Suppress unused-import warnings if a particular template branch
	// didn't reference a package.
	out.P("var _ = ", ctxType, "(nil)")
	out.P()
}

// capitalize returns s with its first rune upper-cased.
func capitalize(s string) string {
	if s == "" {
		return s
	}
	return string(s[0]-32) + s[1:]
}
